    protected void datatable() {
        Object[] Baris = {"idBarang","Nama","Jumlah","Harga"};
        tabmode = new DefaultTableModel(null, Baris);
        tbltrans1.setModel(tabmode);
        String sql = "select * from stokbarang";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
               String id_bar = hasil.getString("idBarang");
               String nm_bar = hasil.getString("Nama");
               String jum_bar = hasil.getString("Jumlah");
               String har_bar = hasil.getString("Harga");
               
               String[] data = {id_bar, nm_bar, jum_bar, har_bar};
                tabmode.addRow(data);
            }
        } catch (Exception e) {

        }
    }

