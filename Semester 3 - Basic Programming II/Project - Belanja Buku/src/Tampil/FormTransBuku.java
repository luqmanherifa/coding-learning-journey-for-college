    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String sql1 = "Insert into transaksi (idtransaksi, idbarang, idpegawai, jumlah, total) values (?,?,?,?,?)";
        String sql2 = "Update stokbarang set jumlah='jumlah-"+txtTJum+"' where idbarang='"+txtTBar.getText()+"'";
        int sb = Integer.parseInt(txtTHar.getText())*Integer.parseInt(txtTJum.getText());
        try{
            PreparedStatement stat1 = conn.prepareStatement(sql1); //menyiapkan data tapi belum disubmit
            PreparedStatement stat2 = conn.prepareStatement(sql2);
            stat1.setString(1, txtidtrans.getText());
            stat1.setString(2, txtTBar.getText());
            stat1.setString(3, txtTPeg.getText());
            stat1.setString(4, txtTJum.getText());
            stat1.setString(5, Integer.toString(sb));

            stat1.executeUpdate();
            
            String sql3     ="SELECT SUM(total) AS total FROM transaksi WHERE idtransaksi='"+txtidtrans.getText()+"'";
            Statement stati = conn.createStatement();
            ResultSet rs  = stati.executeQuery(sql3);
            int stotal = 0;
            int diskon;
            String data_diskon;
            if(rs.next())
            {
                System.out.println(rs.getString("total"));
                stotal = Integer.parseInt(rs.getString("total"));
            }
            
            if(stotal>=300000){
                diskon =(30*stotal/100);
                diskon = stotal-diskon;
                data_diskon = "30%";
            }
            else if(stotal>=200000){
                diskon =(20*stotal/100);
                diskon = stotal-diskon;
                data_diskon = "20%";
            }
            else if(stotal>=150000){
                diskon =(15*stotal/100);
                diskon = stotal-diskon;
                data_diskon = "15%";
            }
            else if(stotal>=100000){
                diskon =(10*stotal/100);
                diskon = stotal-diskon;
                data_diskon = "10%";
            }
            else{
                diskon = stotal;
                data_diskon ="0%";
            }
            lbltotal.setText(Integer.toString(stotal));
            lbldiskon.setText(data_diskon);
            lbltotdis.setText(Integer.toString(diskon));
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "DATA GAGAL DISIMPAN "+e);
        }
    }//GEN-LAST:event_btnTambahActionPerformed

