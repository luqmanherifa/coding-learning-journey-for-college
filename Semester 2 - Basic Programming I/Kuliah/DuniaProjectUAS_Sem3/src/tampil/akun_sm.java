    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        String sql = "UPDATE karyawan SET namakar=?, emailkar=?, usernamekar=?, passwordkar=? WHERE idkar='"+txtID.getText()+"'";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            
            if(txtID.getText().equals("")||txtNamaKar.getText().equals("")||txtEmail.getText().equals("")
                    ||txtUsername.getText().equals("")||txtPassword.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Input data terlebih dahulu!","ERROR", JOptionPane.ERROR_MESSAGE);
            return;
            } else {
                stat.setString(1, txtNamaKar.getText());
                stat.setString(2, txtEmail.getText());
                stat.setString(3, txtUsername.getText());
                stat.setString(4, txtPassword.getText());
            
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil diupdate!");
            
                kosong();
                txtID.requestFocus();
                datatable();
            }
        } catch (Exception except) {
            JOptionPane.showMessageDialog(null, ("Error!\n"+except));
        }
    }//GEN-LAST:event_btnEditActionPerformed

