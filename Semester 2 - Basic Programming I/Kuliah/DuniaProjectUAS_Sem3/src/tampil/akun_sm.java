    protected void datatable() {
        Object[] Baris = {"ID","Nama","Email","Username","Password"};
        tabmode = new DefaultTableModel(null, Baris);
        tblAkun.setModel(tabmode);
        String sql = "SELECT * FROM karyawan";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
               String id_kar = hasil.getString("idkar");
               String nama_kar = hasil.getString("namakar");
               String email_kar = hasil.getString("emailkar");
               String user_kar = hasil.getString("usernamekar");
               String pass_kar = hasil.getString("passwordkar");
               
               String[] data = {id_kar, nama_kar, email_kar, user_kar, pass_kar};
               tabmode.addRow(data);
            }
        } catch (Exception e) {

        }
    }    
    
