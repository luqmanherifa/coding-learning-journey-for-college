    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String sql = "UPDATE menu_service SET namapel=?, kendaraanpel=?, tanggal=?, keluhanpel=? WHERE idservice='"+txtID.getText()+"'";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            
            if(txtID.getText().equals("")||txtNamaPel.getText().equals("")||txtKendaraan.getText().equals("")
                    ||txtTanggal.getText().equals("")||txtKeluhan.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Input data terlebih dahulu!","ERROR", JOptionPane.ERROR_MESSAGE);
            return;
            } else {
                stat.setString(1, txtNamaPel.getText());
                stat.setString(2, txtKendaraan.getText());
                stat.setString(3, txtTanggal.getText());
                stat.setString(4, txtKeluhan.getText());
            
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil diupdate!");
            
                kosong();
                txtID.requestFocus();
                datatable();
            }
        } catch (Exception except) {
            JOptionPane.showMessageDialog(null, ("Error!\n"+except));
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int ok = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin akan menghapus data?", "Hapus Data",JOptionPane.YES_NO_OPTION);
        if(ok==0){
            String sql = "DELETE FROM menu_service WHERE idservice = '"+txtID.getText()+"'";
            try{
                PreparedStatement stat = conn.prepareStatement(sql);
                
                if(txtID.getText().equals(""))
                {
                    JOptionPane.showMessageDialog(null, "Input Nomor Transaksi terlebih dahulu!","ERROR", JOptionPane.ERROR_MESSAGE);
                return;
                } else {  
                    stat.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus! ");
                
                    kosong();
                    txtID.requestFocus();
                    datatable();
                }
            } catch (Exception except) {
                JOptionPane.showMessageDialog(null, ("Error!\n"+except));
            }               
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

