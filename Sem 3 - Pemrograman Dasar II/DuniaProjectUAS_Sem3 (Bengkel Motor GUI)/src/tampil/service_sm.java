    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        String sql = "INSERT INTO menu_service"
                + "(idservice, namapel, kendaraanpel, tanggal, keluhanpel) VALUES (?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);

            if(txtID.getText().equals("")||txtNamaPel.getText().equals("")||txtKendaraan.getText().equals("")
                    ||txtTanggal.getText().equals("")||txtKeluhan.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Input data terlebih dahulu!","ERROR", JOptionPane.ERROR_MESSAGE);
            return;
            } else {
                data.add(txtID.getText());
                data.add(txtNamaPel.getText());
                data.add(txtKendaraan.getText());
                data.add(txtTanggal.getText());
                data.add(txtKeluhan.getText());                

                for(int i=0; i<data.size(); i++){
                    stat.setString(i+1,data.get(i));
                }
                
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil diinsert!");
            
                kosong();
                txtID.requestFocus();
                datatable();
            }
        } catch (Exception except) {
            JOptionPane.showMessageDialog(null, ("Error!\n"+except));
        }
    }//GEN-LAST:event_btnInsertActionPerformed

