    protected void datatable() {
        Object[] Baris = {"No","Nama","Kendaraan","Tanggal","Keluhan"};
        tabmode = new DefaultTableModel(null, Baris);
        tblService.setModel(tabmode);
        String sql = "SELECT * FROM menu_service";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
               String id_serv = hasil.getString("idservice");
               String nama_pel = hasil.getString("namapel");
               String kend_pel = hasil.getString("kendaraanpel");
               String tanggal = hasil.getString("tanggal");
               String kel_pel = hasil.getString("keluhanpel");
               
               String[] data = {id_serv, nama_pel, kend_pel, tanggal, kel_pel};
                tabmode.addRow(data);
            }
        } catch (Exception e) {

        }
    }

