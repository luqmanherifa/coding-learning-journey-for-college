    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try{
            masukin();
        
            String sql = "INSERT INTO tabel_headset"
                + "(kdhs, merkhs, tipehs, warnahs, modelearcup, warnaheadband,"
                + "hargahs, stokhs) VALUES (?,?,?,?,?,?,?,?)";
        
            PreparedStatement stat = conn.prepareStatement(sql);

            if(txtKodeHS.getText().equals("")||txtMerkHS.getText().equals("")||txtTipeHS.getText().equals("")
                    ||txtWarnaHS.getText().equals("")||txtEarcupHS.getText().equals("")||txtHeadbandHS.getText().equals("")
                    ||txtHargaHS.getText().equals("")||txtStokHS.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Input data terlebih dahulu!","ERROR", JOptionPane.ERROR_MESSAGE);
            } else {
                stat.setString(1, txtKodeHS.getText());
                stat.setString(2, txtMerkHS.getText());
                stat.setString(3, txtTipeHS.getText());
                stat.setString(4, txtWarnaHS.getText());
                stat.setString(5, txtEarcupHS.getText());
                stat.setString(6, txtHeadbandHS.getText());
                stat.setInt(7, Integer.valueOf(txtHargaHS.getText()));
                stat.setInt(8, Integer.valueOf(txtStokHS.getText()));
                
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil diinsert!");
                
                kosong();
                txtKodeHS.requestFocus();
                datatable();    
            }
        } catch (Exception except) {
            JOptionPane.showMessageDialog(null, ("Error!\n"+except));
        }
    }//GEN-LAST:event_btnSaveActionPerformed

