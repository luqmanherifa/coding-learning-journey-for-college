    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int ok = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin akan menghapus data?", "Hapus Data",JOptionPane.YES_NO_OPTION);
        if(ok==0){
            String sql = "DELETE FROM tabel_headset WHERE kdhs = '"+txtKodeHS.getText()+"'";
            try{
                PreparedStatement stat = conn.prepareStatement(sql);
                
                if(txtKodeHS.getText().equals(""))
                {
                    JOptionPane.showMessageDialog(null, "Input Nomor Transaksi terlebih dahulu!","ERROR", JOptionPane.ERROR_MESSAGE);
                return;
                } else {  
                    stat.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus! ");
                
                    kosong();
                    txtKodeHS.requestFocus();
                    datatable();
                }
            } catch (Exception except) {
                JOptionPane.showMessageDialog(null, ("Error!\n"+except));
            }               
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

